class Meta {
  /// meta dir
  final String? dir;

  /// meta id
  final String? id;

  /// meta property
  final String property;

  /// meta refines
  final String? refines;

  /// meta schema
  final String? scheme;

  /// meta xmlLang
  final String? xmlLang;

  Meta({
